# To remove all the dictionary's items, you need to use the "clear()" method
pol_eng_dictionary = {
    "zamek": "castle",
    "woda": "water",
    "gleba": "soil"
    }
 
print(len(pol_eng_dictionary)) # outputs: 3
 
pol_eng_dictionary.clear() # removes all the items
print(len(pol_eng_dictionary)) # outputs: 0

